char	*ft_revitoa(char *str, long int num)
{
	int	i;

	i = 0;
	if (num < 0)
	{
		str[i] = '-';
		i++;
		num = (num * (-1));
	}
	else if (num == 0)
	{
		str[i] = '0';
		i++;
	}
	while (num)
	{
		str[i] = '0' + (num % 10);
		i++;
		num = (num / 10);
	}
	str[i] = '\0';
	return (str);
}
