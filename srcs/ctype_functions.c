int	ft_isspace(int c)
{
	return (c == '\n' || c == '\t' || c == '\v'
		|| c == '\r' || c == '\f' || c == ' ');
}

int	ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}
